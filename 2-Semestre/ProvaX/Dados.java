package zProva;

public class Dados {
    private String cidade;
    private int totalCarros;
    private int totalMotos;
    private int totalCaminhoes;
    private int total;
    private String estadociv;

    public String getCidade() {
        return cidade;
    }
    public void setCidade(String cidade) {
        this.cidade = cidade;
    }
    public int getTotalCarros() {
        return totalCarros;
    }
    public void setTotalCarros(int totalCarros) {
        this.totalCarros = totalCarros;
    }
    public int getTotalMotos() {
        return totalMotos;
    }
    public void setTotalMotos(int totalMotos) {
        this.totalMotos = totalMotos;
    }
    public int getTotalCaminhoes() {
        return totalCaminhoes;
    }
    public void setTotalCaminhoes(int totalCaminhoes) {
        this.totalCaminhoes = totalCaminhoes;
    }
    public int getTotal() {
        return total;
    }
    public void setTotal(int total) {
        this.total = total;
    }
    public String getEstadociv() {
        return estadociv;
    }
    public void setEstadociv(String estadociv) {
        this.estadociv = estadociv;
    }
    

   
}
